 
a = 10
b = 3

# Arithmetic operators
print(a + b)
print(a - b)
print(a * b)
print(a / b)
print(a // b) #floor division, returns divison and round down to the nearest integer
print(a % b) ## modulo, returns the remainder of a division
print(a ** b)

# Comparison - always return True or False
print(10 > 3)
print(10 == 10)
print(10 != 5)
print(10 < 3)

# Logical - combining conditions
print(True and False)
print(True or False)
print(not True)

